//print numbers from 0 to 10

class loop1{
    public static void main(String[] args){

        System.out.println("using for loop");

     for(int i =0;i<=10;i++){                 //for loop
        System.out.println(i);
     }



     
     //while loop
     int i =0;
     System.out.println("using while loop");
     while(i<=10){
        
        System.out.println(i);

        i++;
     }
    }
}
